# frozen_string_literal: true

puts 1+2 #=> 3
puts 'a'+'b' #=> 'ab'

# Plus / Minus
puts 1 - 2 #=> -1
puts 1 + 2 #=> 3

# Multiple
puts 4 * 2 #=> 8
puts 4 * 2.1 #=> 8.4
puts [1, 'a'] * 2 #=> [1, 'a', 1, 'a']
puts 'a' * 2 #=> 'aa'

# Square
puts 4 ** 2 #=> 16

# Division & Mod
puts 5 / 2 #=> 2
puts 5 % 2 #=> 1
